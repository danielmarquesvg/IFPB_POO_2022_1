#include "Triangulo.h"

#include <string>
using namespace std;

Triangulo::Triangulo(double novaBase, double novaAltura, string novoNome, double novoTamanhoDoLado, double novaQuantidadeDeLados) : FiguraGeometrica (novoNome, novoTamanhoDoLado, novaQuantidadeDeLados) {
	this->base = novaBase;
	this->altura = novaAltura;
}

double Triangulo::calcularArea(double base, double altura){
	double area = (base * altura) / 2;
	return area;
}

